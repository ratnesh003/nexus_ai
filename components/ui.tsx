export const Badge: React.FC<{
  children: React.ReactNode;
  variant?: "outline" | "default";
}> = ({ children, variant = "default" }) => {
  const style =
    variant === "outline"
      ? "border border-border text-foreground"
      : "bg-accent text-white";
  return (
    <span
      className={`inline-flex items-center rounded-full px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2 ${style}`}
    >
      {children}
    </span>
  );
};

